import 'package:chewie/chewie.dart';
import 'package:flutter/material.dart';
import 'package:video_player/video_player.dart';

class VideoPlayerScreen extends StatefulWidget {
  @override
  _VideoPlayerScreenState createState() => _VideoPlayerScreenState();
}

class _VideoPlayerScreenState extends State<VideoPlayerScreen> {
  late VideoPlayerController _videoPlayerController;
  late ChewieController _chewieController;

  @override
  void initState() {
    super.initState();

    // Khởi tạo VideoPlayerController
    _videoPlayerController = VideoPlayerController.networkUrl(
      Uri.parse(
          "https://csairs.website/media/eduhub-video/efefbba7-a081-4ab2-9903-dafe420aee46"), // Link video của bạn
    )..initialize().then((_) {
        setState(() {}); // Cập nhật trạng thái sau khi video được khởi tạo
      });

    // Khởi tạo ChewieController
    _chewieController = ChewieController(
      videoPlayerController: _videoPlayerController,
      autoPlay: true,
      looping: true,
      startAt: Duration(seconds: 0),

    );
  }

  @override
  void dispose() {
    _videoPlayerController.dispose();
    _chewieController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('Video Player with Chewie'),
      ),
      body: Center(
        child: _videoPlayerController.value.isInitialized
            ? Chewie(controller: _chewieController)
            : CircularProgressIndicator(),
      ),
    );
  }
}

